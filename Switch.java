package yuan;

public class Switch {
	
	public static void main(String args[]) {
		char c = 'd';
		
		switch(c) {
			case 'a':
				System.out.println("a");
				break;
			case 'b':
				System.out.println("b");
				break;
			default:
				System.out.println("default");
		
		}
	}

}
