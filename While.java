package yuan;

public class While {

	public static void main(String args[]) {
		// while
		int i = 1;
		
		while(i < 10) {
			System.out.print(i);
			i ++;
		}
		
		System.out.println("");
		
		// do-while
		int j = 1;
		do {
			System.out.print(j);
			j ++;
		} while (j < 10);
		
	}
}
