package yuan;

public class if_else {

	public static void main(String args[]) {
		int x = -2;
		
		if (x > 0) {
			System.out.println("This number is greater than 0.");
		} else if (x == 0) {
			System.out.println("This number is equal to 0.");
		} else {
			System.out.println("This number is less than 0.");
		}
		
	}
}
