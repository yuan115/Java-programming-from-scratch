package yuan;

public class Cast {
	
	public static void main(String args[]) {
		
		int i = 10;
		long l = (long)i;
		
		System.out.println(l);
		
		long long_from_int = 404191023108323029L;
		float float_from_double = 3.14F;
		
		System.out.println(long_from_int);
		System.out.println(float_from_double);
		
	}

}
